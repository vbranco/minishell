#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*tab;

	i = 0;
	tab = (char*)s;
	if (n == 0)
		return ;
	else
	{
		while (i < n)
		{
			tab[i] = '\0';
			i++;
		}
	}
}
