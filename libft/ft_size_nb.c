#include "libft.h"

int		ft_size_nb(int n)
{
	int		size;

	size = 0;
	if (n < 0)
	{
		n = n * -1;
		size++;
	}
	while (n > 9)
	{
		size++;
		n /= 10;
	}
	return (size + 1);
}
